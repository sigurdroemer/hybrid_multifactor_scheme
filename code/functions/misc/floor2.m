function y = floor2(x)
%
%   Implements Matlab's 'floor' function with a small pertubation to avoid problems with 
%   round-off errors.
%
% -----------------------------------------------------------------------------------------------
%  Parameters
% -----------------------------------------------------------------------------------------------
%   x:  [NxM real] Input matrix.
% -----------------------------------------------------------------------------------------------
%  Output
% -----------------------------------------------------------------------------------------------
%   y:  [NxM real] Floored values.
% -----------------------------------------------------------------------------------------------

    y = floor(x + eps(x));

end

