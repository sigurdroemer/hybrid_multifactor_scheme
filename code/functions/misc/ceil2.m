function y = ceil2(x)
%
%   Implements Matlab's 'ceil' function with a small pertubation to avoid problems with 
%   round-off errors.
%
% -----------------------------------------------------------------------------------------------
%  Parameters
% -----------------------------------------------------------------------------------------------
%   x:  [NxM real] Input matrix.
% -----------------------------------------------------------------------------------------------
%  Output
% -----------------------------------------------------------------------------------------------
%   y:  [NxM real] Floored values.
% -----------------------------------------------------------------------------------------------

    y = ceil(x - eps(x));

end